#include <unistd.h>

int	ft_sqrt(int nb)
{
	long	i;
	long	n;

	n = nb;
	i = 1;
	while (i * i <= n && n > 0)
	{
		if (i * i == n)
			return (i);
		i++;
	}
	return (0);
}
