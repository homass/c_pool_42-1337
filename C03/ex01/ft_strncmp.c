#include <unistd.h>

int	ft_strncmp(char *s1, char *s2, unsigned int size)
{
	int	i;

	i = 0;
	while (s1[i] && s2[i] && (s1[i] - s2[i] == 0) && size)
	{
		i++;
		size--;
	}
	if (size == 0)
		return (0);
	else
		return (s1[i] - s2[i]);
}
